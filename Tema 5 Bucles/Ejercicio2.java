/**
*	Ejercicio 2
*	Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.
**/
public class Ejercicio2{
	public static void main (String[]args){
		int numero = 0;
		while(numero<100){
		numero += 5;
		System.out.println("El número " + numero + " es múltiplo de cinco.");
		}
	}
}
